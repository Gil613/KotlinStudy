//산술연산자
val number1: Int = 1 + 10
val number2: Int = 2 - 10
val number3: Int = 3 * 10
val number4: Int = 2 / 10
val number5: Int = 5 % 3
println(number1)
println(number2)
println(number3)
println(number4)
println(number5)

//복합 대입 연산자
var plus: Int = 1
var min: Int = 1
plus += 1
min -= 1
println(plus)
println(min)


//증감 연산자
var a: Int = 1
var b: Int = 1
println(a)
a++
println(a)
println(b)
++b
println(b)

//비교 연산자 ==, >, <, >=, <=, !=, ===, !==
var eq: Int = 1
var eq2: Int = 1
var exc: Int = 5
var under: Int = 5
var more: Int = 3
var less: Int = 3
println(eq == 1)
println(eq != 1)
println(exc > 4)
println(exc > 5)
println(under < 11)
println(under < 10)
println(more >= 3)
println(less <= 3)
println(eq === eq2)
println(eq == eq2)

